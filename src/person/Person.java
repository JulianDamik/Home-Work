package person;

public class Person {
	
	private String fName;
	private String lName;
	private int age;
	private double weight; 
	
	public void setFName(String name){
		fName = name;
	}
	public void setLName(String name){
		lName = name;
	}
	public void setAge(int ag){
		age = ag;
	}
	public void setWeight(double w){
		weight = w;
	}
}
	